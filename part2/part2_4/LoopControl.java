package part2.part2_4;

public class LoopControl {
    public static void main(String[] args) {
        System.out.println("--- Первое простое > 100 ---");
        int n = 101;
        outer:
        while (true) {
            if (n % 2 == 0 && n != 2) {
                n++;
                continue;
            }
            for (int d = 3; d * d <= n; d += 2) {
                if (n % d == 0) {
                    n++;
                    continue outer;
                }
            }
            break;
        }
        System.out.println("Первое простое > 100: " + n);

        System.out.println("\n--- Нечётные, не кратные 3 (от 1 до 20) ---");
        for (int i = 1; i <= 20; i++) {
            if (i % 2 == 0) continue;
            if (i % 3 == 0) continue;
            System.out.print(i + " ");
        }
        System.out.println();

        System.out.println("\n--- Поиск в матрице ---");
        int[][] matrix = {
                {1, 2, 3},
                {4, 5, 6},
                {7, 8, 9}
        };
        int target = 5;
        int foundRow = -1, foundCol = -1;

        search:
        for (int row = 0; row < matrix.length; row++) {
            for (int col = 0; col < matrix[row].length; col++) {
                if (matrix[row][col] == target) {
                    foundRow = row;
                    foundCol = col;
                    break search;
                }
            }
        }

        if (foundRow != -1) {
            System.out.printf("Число %d найдено на позиции [%d][%d]%n", target, foundRow, foundCol);
        }

        // TODO: объясните в комментарии ниже 3 пункта из задания 2.4.
        // ▼ ВАШ КОД ЗДЕСЬ ▼
        // 1) Метки (outer: и search:) позволяют управлять вложенными циклами. Обычно break и continue работают
        //    только с самым внутренним циклом. Метка позволяет указать, из какого именно цикла нужно выйти (break)
        //    или продолжить выполнение (continue). Метка ставится перед циклом и отделяется двоеточием.
        //    В данном случае search: помечает внешний for-цикл, и break search; прерывает сразу оба вложенных цикла,
        //    выходя к строке после внешнего цикла.
        //
        // 2) continue outer; в проверке делителей работает следующим образом: когда находим делитель d, на который n
        //    делится без остатка, значит n — составное. Увеличиваем n на 1 и с помощью continue outer; переходим
        //    к следующей итерации помеченного внешнего while-цикла. Это позволяет сразу проверить следующее
        //    число на простоту, без необходимости вложенных условий.
        //
        // 3) Вот результаты выполнения трёх блоков:
        //    Блок 1: "Первое простое > 100: 101" — 101 является простым числом.
        //    Блок 2: "1 5 7 11 13 17 19 " — нечётные числа от 1 до 20,
        //            которые не делятся на 3 (пропущены 3, 9, 15).
        //    Блок 3: Число 5 найдено на позиции [1][1] — поиск в матрице завершается сразу после
        //    нахождения целевого числа.
        // ▲ КОНЕЦ ВАШЕГО КОДА ▲
    }
}